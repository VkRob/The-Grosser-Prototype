package engine.render.shader;

public class ShaderUniform {
	private int id;
	private String name;

	public ShaderUniform(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
